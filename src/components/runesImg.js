function importAll(r){
    let itemsImgs = {};

    r.keys().map((item) => {
        itemsImgs[item.replace('./', '')] = r(item);
    });

    return itemsImgs;
}

const itemsImgs = importAll(require.context("../assets/loldata/current/img/item/", false, /\.(png)$/));

export const getItemImg = (id) => {
    return itemsImgs[`${id}.png`];
}

const summonersImgs = importAll(require.context("../assets/loldata/current/img/summoner/", false, /\.(png)$/));

